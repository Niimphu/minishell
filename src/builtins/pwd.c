#include "../../minishell.h"

int	pwd(t_god *god_struct)
{
	char	*path;

	path = get_env_var("PWD=", god_struct->env, TRIM);
	if (!path)
		return (1);
	ft_putstr_fd(path, STDOUT_FILENO);
	ft_putstr_fd("\n", STDOUT_FILENO);
	return (0);
}
